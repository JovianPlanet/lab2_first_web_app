"""
Module that contains functions to perform mathematical operations
"""

def summation(a, b):
    """
    Adds two numbers a, b

    returns: a + b
    """
    result = a + b
    return result

def substraction(a, b):
    """
    Substracts b from a

    returns: a - b
    """
    result = a - b
    return result

def multiplication(a, b):
    """
    Computes b times a

    returns: a * b
    """
    result = a * b
    return result
    